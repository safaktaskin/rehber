
package com.uniyaz;

public class Kisi {

        private String ad;
        private String soyad;
        private String numara;

        public String getAd() {
            return ad;
        }

        public void setAd(String ad) {
            this.ad = ad;
        }


        public String getSoyad() {
            return soyad;
        }
        public void setSoyad(String soyad) {
            this.soyad = soyad;
        }
        public String getNumara() {
            return numara;
        }
        public void setNumara(String numara) {
            this.numara = numara;
        }


        public Kisi() {
            this.ad = ad;
            this.soyad=soyad;
            this.numara = numara;
        }
}
